# Meminta input suhu dalam Celsius dari user
celsius = float(input("Masukkan suhu dalam Celsius: "))

# Mengonversi suhu dari Celsius ke Fahrenheit
fahrenheit = (9 / 5) * celsius + 32

# Menampilkan hasil konversi
print("Suhu dalam Fahrenheit:", fahrenheit)

# contoh output:
# jika user memasukan:

# "masukkan suhu dalam celsius: 25"

# program akan menghitung sebagai berikut:

# ["\text{Fahrenheit} = (\text{Celcius} \times \frac{9}{5}) + 32 = 45 + 32 = 77"]

# sehingga output yg akan di hasilkan adalah:

# "suhu dalam fahrenheit: 77.0"
