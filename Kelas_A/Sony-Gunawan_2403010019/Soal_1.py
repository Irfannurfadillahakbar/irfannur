# menghitung luas dan keliling persegi panjang

# input panjang dan lebar
panjang = float(input("masukan panjang"))
lebar = float(input("masukan lebar"))

# Menghitung luas
luas = panjang * lebar

# menghitung keliling
keliling = 2 * (panjang + lebar)

# menampilkan hasil
print("Luas persegi panjang itu adalah:", luas)
print("keliling persegi panjang itu adalah: ",keliling)